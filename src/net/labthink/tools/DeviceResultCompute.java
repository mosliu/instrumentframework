    private void jButton_RGT_computeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_RGT_computeActionPerformed
        double P1 = getDoubleFromjFormattedTextField(jFormattedTextField_RGT_P1);
        double P2 = getDoubleFromjFormattedTextField(jFormattedTextField_RGT_P2);
        double H1 = getDoubleFromjFormattedTextField(jFormattedTextField_RGT_H1);
        double H2 = getDoubleFromjFormattedTextField(jFormattedTextField_RGT_H2);
        double H0 = getDoubleFromjFormattedTextField(jFormattedTextField_RGT_H0);
        double S = getDoubleFromjFormattedTextField(jFormattedTextField_RGT_S);
        double T0 = getDoubleFromjFormattedTextField(jFormattedTextField_RGT_T0);
        double Hzeropoint = getDoubleFromjFormattedTextField(jFormattedTextField_RGT_HZERO);
        double K = getDoubleFromjFormattedTextField(jFormattedTextField_RGT_K);
        K = K / 0.20948;
        H1 = H1 - Hzeropoint;
        H2 = H2 - Hzeropoint;

        double V0 = (0.1 * S * H2 * (P2 + 0.0098 * (H0 + H2)) - 0.1 * S * H1 * (P1 + 0.0098 * (H0 + H1))) / ((P1 + 0.0098 * (H0 + H1)) - (P2 + 0.0098 * (H0 + H2)));
        double P0 = (P1 + 0.0098 * (H0 + H1)) * (V0 + 0.1 * S * H1) / V0;
        double V = 57.057 * P0 * V0 / (101.3 * (T0 + 273)) * K;
        jTextArea_output.append(null);
        jTextArea_output.setText("结果V0为：" + V0);
        jTextArea_output.append("\r\n结果P0为：" + P0);
        jTextArea_output.append("\r\n最终结果为：" + V + "ml");

    }//GEN-LAST:event_jButton_RGT_computeActionPerformed

